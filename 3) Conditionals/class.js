var highschool=prompt("What grade are you in?")

if (highschool == "9"){
    alert("You're a Freshman");
} else if (highschool == "10") {
    alert("You're a Sophomore");
} else if (highschool == "11") {
    alert("You're a Junior" );
} else if (highschool == "12") {
    alert("You're a senior");
} else {
    alert("You're not even in high school, why are you even pretending to be in high school? Get outta here!");
}
